## 对向量进行操作
### 对向量同时加一个数
c(1, 2, 3, 4, 5) + 1
### 向量之间的运算
c(1, 2, 3, 4, 5) + c(5, 4, 3, 2, 1)
### 不等长向量之间的运算
c(1, 2, 3, 4) + c(4, 3) # 等效于c(1, 2, 3, 4) + c(4, 3, 4, 3)，结果是c(5, 5, 7, 7)

